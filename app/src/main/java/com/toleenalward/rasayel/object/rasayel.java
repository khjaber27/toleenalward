package com.toleenalward.rasayel.object;

public class rasayel {

    int image;
    String namerasayel;

    public rasayel(int image, String namerasayel) {
        this.image = image;
        this.namerasayel = namerasayel;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getNamerasayel() {
        return namerasayel;
    }

    public void setNamerasayel(String namerasayel) {
        this.namerasayel = namerasayel;
    }
}
